            <h4 class="mt-3"><b>Bola Jatuh Bebas</b></h4>
            <h6 class="mb-3">Kode Percobaan: M6</h6>
            <h6><b>I. Tujuan Percobaan</b></h6>
            <p>Menentukan besarnya percepatan gravitasi bumi di suatu tempat.</p>

            <h6><b>II. Peralatan Yang Digunakan</b></h6>
            <ol>
              <li>Large contact plate include steel ball (1 set)</li>
              <li>Holding magnet with multiclamp (1 buah)</li>
              <li>Holding magnet adapter with a release mechanism (1 buah)</li>
              <li>Counter S (1 buah)</li>
              <li>Stand bases MF (2 buah)</li>
              <li>Stand rods 10 mm, 25 cm (3 buah)</li>
              <li>Stand rod 12 mm, 150 cm (1 buah)</li>
              <li>Leybold multiclamps (2 buah)</li>
              <li>Scale with pointers (1 buah)</li>
            </ol>

            <h6><b>III. Teori</b></h6>
            <p>Ketika sebuah benda jatuh di dalam medan gravitasi bumi dari ketinggian h di atas tanah, benda akan mengalami percepatan konstan, bila jarak jatuhnya pendek dan gesekan dengan udara diabaikan. Gerakan semacam ini disebut gerak jatuh bebas. Apabila sebuah benda dijatuhkan dari ketinggian tertentu maka benda tersebut akan mengalami percepatan sebesar percepatan gravitasi bumi di tempat tersebut, a = g.</p>
            <p>Jika benda dijatuhkan bebas dari ketinggian tertentu di atas permukaan bumi pada waktu t<sub>0</sub> = 0 dengan kecepatan awal v<sub>0</sub> = 0, jarak yang ditempuh dalam waktu t adalah:</p>
            <div class="row justify-content-center mb-4">
              <img src="http://localhost/fisdas/assets/img/modul9-1.png" width="80%" alt="">
            </div>
