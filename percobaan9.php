            <p>Bila jarak tempuh (h) suatu benda yang jatuh bebas diketahui dengan waktu tempuh sebesar t, maka nilai percepatan gravitasi g di tempat benda jatuh bebas dapat ditentukan.</p>
            <div class="row justify-content-center mb-4">
