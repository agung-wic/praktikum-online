            <h6><b>IV. Cara Melakukan Percobaan:</b></h6>
            <ol>
              <li>Susun peralatan seperti Gambar 4.11.</li>
              <li>Hubungkan adaptor pada sumber listrik.</li>
              <li>Tentukan jarak (h) 80 cm dengan jalan mengatur ketinggian holding magnet dan pelat kontak.</li>
              <li>Atur mode Counter S pada t <sub>E→F</sub> (satuan : ms) dengan menekan tombol MODE beberapa kali.</li>
              <li>Letakkan bola besi pertama pada holding magnet. Tekan kembali pelat kontak ke posisi nol.</li>
              <li>Tekan tombol Start sehingga LED menyala.</li>
              <li>Tekan tombol Saklar (LD 336 25) dengan cepat untuk memulai bola jatuh bebas.</li>
              <li>Saat bola menyentuh pelat kontak, baca waktu dan catat hasilnya.</li>
              <li>Kurangi jarak jatuh dengan mengatur holding magnet dan pelat kontak.</li>
              <li>Ulangi langkah percobaan nomor 5-8 pada bola besi kedua dengan jarak yang sama. <b>(pastikan tekan kembali pelat kontak pada posisi nol)</b></li>
              <li>Ulangi langkah 3-8 pada dua macam bola dan masing – masing ketinggian 20 cm, 40 cm, dan 100 cm.</li>
              <li>Lakukan pengambilan data sebanyak 5 kali untuk masing – masing bola dan ketinggian.</li>
            </ol>

            <h6><b>V. Tugas Untuk Laporan Resmi:</b></h6>
            <ol>
              <li>Hitung waktu rata-rata untuk setiap bola pada ketinggian tertentu.</li>
              <li>Buat grafik antara h terhadap t<sup>2</sup> untuk setiap bola.</li>
              <li>Hitung percepatan gravitasi dari table dan gambar.</li>
              <li>Buat kesimpulan dari percobaan ini.</li>
            </ol>

