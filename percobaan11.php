              <li>Atur waktu pengukuran maksimum (tanya Asisten)</li>
              <li>Klik measurement.(Mulai pengukuran dengan <i class="fas fa-stopwatch    "></i> (troli mulai bergerak).</li>
              <li>Pengukuran berhenti secara otomatis sesuai batas yang telah ditentukan sebelumnya.</li>
              <li>Lihat tampilan dilayar monitar untuk masing-masing lintasan (catat posis dan kecepatan pada tiap waktu)</li>
              <li>Hitung percepatan benda dari grafik</li>
              <li>Ulangi untuk beban yang lain (tambahkan beban 10 gram untuk masing-masing pengulangan) (Cek tampilan Path SA1 jika tidak nol tekan tombol →0←)</li>
              <li>Copy tabel untuk lintasan dan kecepatan kedalam notepad</li>
