            <ol>
              <li>Susunlah rangkain alat seperti pada Gambar 4.13.</li>
              <li>Gantungkan beban F dengan meletakkan massa 10 gram</li>
              <li>tambahkan 500 gram di atas troli yang massanya 498 gram,</li>
              <li>Nyalakan PC</li>
              <li>Atur holding magnet sehingga mampu menahan tegangan tali, sifat magnet akan hilang secara bersamaan dengan dimulainya pengukuran waktu.</li>
              <li>Atur panjang lintasan</li>
